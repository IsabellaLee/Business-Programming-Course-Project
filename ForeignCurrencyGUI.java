    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Select the currency first");
        } else if (user.getBrokerage().getForeignCurrencyList().get(selectedIndex).getVolume() != 0) {
            JOptionPane.showMessageDialog(null, "Sell first");
        } else {
            ForeignCurrency f = user.getBrokerage().getForeignCurrencyList().get(selectedIndex);
        
        user.getBrokerage().getForeignCurrencyList().remove(f);
        user.getBrokerage().getInvestmentAssetList().remove(f);
     
            m1.remove(selectedIndex);
       
        jDialog2.setSize(300,300);
        jDialog2.setVisible(true); 
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

