    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        double volume = Double.parseDouble(jTextField6.getText());
        double exchangeRate = Double.parseDouble(jTextField5.getText());
        int selectedIndex = jList1.getSelectedIndex();
        double settlementAmount = user.getBrokerage().getSettlement();
        double originalVolume = user.getBrokerage().getForeignCurrencyList().get(selectedIndex).getVolume();
        if (originalVolume - volume >= 0) {
            user.getBrokerage().getForeignCurrencyList().get(selectedIndex).setVolume(originalVolume - volume);
            user.getBrokerage().setSettlement(settlementAmount+volume);   
            
            user.getBrokerage().getForeignCurrencyList().get(selectedIndex).setExchangeRate(exchangeRate);
            m1.set(selectedIndex, user.getBrokerage().getForeignCurrencyList().get(selectedIndex).toString());
        } else {
            JOptionPane.showMessageDialog(null, "Huh, you are trying to sell something you don't have, smart!");
        }
         jDialog4.dispose();
    }//GEN-LAST:event_jButton9ActionPerformed

