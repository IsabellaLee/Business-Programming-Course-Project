    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
                // TODO add your handling code here:
        double volume = Double.parseDouble(jTextField4.getText());
        double exchangeRate = Double.parseDouble(jTextField3.getText());
        int selectedIndex = jList1.getSelectedIndex();
        double crtDeposit = user.getBrokerage().getDeposit();
        if (volume / exchangeRate > crtDeposit) {
             JOptionPane.showMessageDialog(null, "Insufficient Fund.");
        } else {
            user.getBrokerage().setDeposit(crtDeposit - volume / exchangeRate);
            double originalVolume = user.getBrokerage().getForeignCurrencyList().get(selectedIndex).getVolume();
            user.getBrokerage().getForeignCurrencyList().get(selectedIndex).setExchangeRate(exchangeRate);

            user.getBrokerage().getForeignCurrencyList().get(selectedIndex).setVolume(originalVolume + volume);
            m1.set(selectedIndex, user.getBrokerage().getForeignCurrencyList().get(selectedIndex).toString());
            jDialog3.dispose();
        }
    }//GEN-LAST:event_jButton8ActionPerformed

