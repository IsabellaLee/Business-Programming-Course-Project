    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        //BUY BOND
        int volume = Integer.parseInt(jTextField7.getText());
        int selectedIndex = jList1.getSelectedIndex();
        Bond selectedBond = user.getBrokerage().getBondList().get(selectedIndex);
        double crtDeposit = user.getBrokerage().getDeposit();
        if (volume * selectedBond.calCurrentValue() > crtDeposit) {
            JOptionPane.showMessageDialog(null, "Insufficient Fund.");
        } else {
            user.getBrokerage().setDeposit(crtDeposit- volume * selectedBond.calCurrentValue());
            int originalVolume = user.getBrokerage().getBondList().get(selectedIndex).getVolume();
            user.getBrokerage().getBondList().get(selectedIndex).setVolume(originalVolume + volume);
            m1.set(selectedIndex, user.getBrokerage().getBondList().get(selectedIndex).toString());
            
            jDialog3.dispose();
        }
    }//GEN-LAST:event_jButton8ActionPerformed

