    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        int volume = Integer.parseInt(jTextField8.getText());
        int selectedIndex = jList1.getSelectedIndex();
        int originalVolume = user.getBrokerage().getBondList().get(selectedIndex).getVolume();
        double settlementAmount = user.getBrokerage().getSettlement();
        double crtValue = user.getBrokerage().getBondList().get(selectedIndex).calCurrentValue();
        if (originalVolume - volume >= 0) {
            user.getBrokerage().setSettlement(settlementAmount +volume * crtValue);
            user.getBrokerage().getBondList().get(selectedIndex).setVolume(originalVolume - volume);
            m1.set(selectedIndex, user.getBrokerage().getBondList().get(selectedIndex).toString());
           
        } else {
            JOptionPane.showMessageDialog(null, "Huh, you are trying to sell something you don't have, smart!");
        }
        
        
        jDialog4.dispose();
    }//GEN-LAST:event_jButton9ActionPerformed

