    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Select the bond first");
        } else if (user.getBrokerage().getBondList().get(selectedIndex).getVolume() != 0) {
            JOptionPane.showMessageDialog(null, "Sell first");
        } else {
            Bond b = user.getBrokerage().getBondList().get(selectedIndex);
            m1.remove(selectedIndex);
            user.getBrokerage().getBondList().remove(b);
            user.getBrokerage().getInvestmentAssetList().remove(b);
            
            jDialog2.setSize(300,300);
            jDialog2.setVisible(true);
        }
       
      
            
    
        
    }//GEN-LAST:event_jButton2ActionPerformed

