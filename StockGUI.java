    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        int volume = Integer.parseInt(jTextField5.getText());
        double closingPrice = Double.parseDouble(jTextField6.getText());
        int selectedIndex = jList1.getSelectedIndex();
        double settlementAmount = user.getBrokerage().getSettlement();
        int originalVolume = user.getBrokerage().getStockList().get(selectedIndex).getVolume();
        if (originalVolume - volume >= 0) {
            user.getBrokerage().getStockList().get(selectedIndex).setClosingPrice(closingPrice);
            user.getBrokerage().setSettlement(settlementAmount+volume * closingPrice);
            user.getBrokerage().getStockList().get(selectedIndex).setVolume(originalVolume - volume);
            m1.set(selectedIndex, user.getBrokerage().getStockList().get(selectedIndex).toString());
            
        } else {
            JOptionPane.showMessageDialog(null, "Huh, you are trying to sell something you don't have, smart!");
        }
        
        jDialog4.dispose();
    }//GEN-LAST:event_jButton9ActionPerformed

