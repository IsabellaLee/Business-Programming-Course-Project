    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jList1.getSelectedIndex();
        
        
        if ( selectedIndex == -1) {
            JOptionPane.showMessageDialog(null, "Select the stock first");
            
        } else if (user.getBrokerage().getStockList().get(selectedIndex).getVolume() != 0) {
           JOptionPane.showMessageDialog(null, "Sell first");
        }else {
            Stock s = user.getBrokerage().getStockList().get(selectedIndex);
            m1.remove(selectedIndex);
            user.getBrokerage().getStockList().remove(s);
            user.getBrokerage().getInvestmentAssetList().remove(s);
        
            
        
            jDialog2.setSize(200,200);
            jDialog2.setVisible(true); 
        }
       

    }//GEN-LAST:event_jButton2ActionPerformed

