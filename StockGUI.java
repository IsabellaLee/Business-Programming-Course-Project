    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int volume = Integer.parseInt(jTextField3.getText());
        double closingPrice = Double.parseDouble(jTextField4.getText());
        int selectedIndex = jList1.getSelectedIndex();
        double crtDeposit = user.getBrokerage().getDeposit();
        if (volume * closingPrice > crtDeposit) {
            JOptionPane.showMessageDialog(null, "Insufficient Fund.");
        } else {
            
            user.getBrokerage().setDeposit(crtDeposit - volume * closingPrice);
            
            user.getBrokerage().getStockList().get(selectedIndex).setClosingPrice(closingPrice);
            int originalVolume = user.getBrokerage().getStockList().get(selectedIndex).getVolume();
            user.getBrokerage().getStockList().get(selectedIndex).setVolume(originalVolume + volume);
            
            m1.set(selectedIndex, user.getBrokerage().getStockList().get(selectedIndex).toString());
            jDialog3.dispose();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

